package com.mobucks.androidsdk.tools.vastparser.models;
public class MediaFile {

	private String value;
	private String id;
	private String delivery;
	private String type;
	private String bitrate;
	private String width;
	private String height;
	private String scalable;
	private String maintainAspectRatio;
	private String apiFramework;


	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDelivery() {
		return delivery;
	}

	public void setDelivery(String delivery) {
		this.delivery = delivery;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getBitrate() {
		return bitrate;
	}

	public void setBitrate(String bitrate) {
		this.bitrate = bitrate;
	}

	public String getWidth() {
		return width;
	}

	public void setWidth(String width) {
		this.width = width;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}

	public String getScalable() {
		return scalable;
	}

	public void setScalable(String scalable) {
		this.scalable = scalable;
	}

	public String getMaintainAspectRatio() {
		return maintainAspectRatio;
	}

	public void setMaintainAspectRatio(String maintainAspectRatio) {
		this.maintainAspectRatio = maintainAspectRatio;
	}

	public String getApiFramework() {
		return apiFramework;
	}

	public void setApiFramework(String apiFramework) {
		this.apiFramework = apiFramework;
	}
}